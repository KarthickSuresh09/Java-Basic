package da4Condition;

public class elseTask3 {
public static void main(String[]args) {
	int a=9;
	if (a >= 8 && a <= 11 ) {
		System.out.println("Good Morning");
	}
	else if (a >= 12 && a <= 15 ){
		System.out.println("Good Noon");
	}
	else if (a >=16 && a <= 20) {
		System.out.println("Good Evening");
	}
	else {
		System.out.println("Good Night");
	}
}
}
